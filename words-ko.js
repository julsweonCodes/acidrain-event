/**
 * Get a shuffled copy of the Korean word list (no duplicates)
 * 한글 단어 목록 섞기 (중복 없음)
 */
function getShuffledWordListKo() {
    const shuffled = [...WORD_LIST_KO];
    for (let i = shuffled.length - 1; i > 0; i--) {
        const j = Math.floor(Math.random() * (i + 1));
        [shuffled[i], shuffled[j]] = [shuffled[j], shuffled[i]];
    }
    return shuffled;
}
